package controlStatementsHypersage;

public class NestedFor {
   public static void main(String[] args) {
	   System.out.println("Multiplication table of 2");
		System.out.println("---------------------------");
	for(int i=2;i<3;i++) {
		for(int j=1;j<=10;j++) {
			int n=i*j;
		
		System.out.println(i+"*"+j+"="+n);
	}
	
}
}
}
